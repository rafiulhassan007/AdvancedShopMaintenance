    private void btnFinaVoucherlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinaVoucherlActionPerformed
        double x = 0;
        double y = 0;
        try {
            x = Double.parseDouble(lvlTotalprice.getText());
            y = Double.parseDouble(tbxPaid.getText());
        } catch (Exception e) {
        }
        int dialogResult = JOptionPane.showConfirmDialog(this, "Are You Sure The Amount of " + y + " Was Paid?", "Confirmation!", JOptionPane.YES_NO_OPTION);
        if (dialogResult == JOptionPane.YES_OPTION) {
            if (x > 0 && y >= 0 && !tbxSearchMob.getText().equals("") && !tbxSearchMob.getText().equals("Phone No.") && !tbxCustoname.getText().equals("") && !tbxcustoaddress.getText().equals("")) {
                int rowcount = model.getRowCount();
                String pidarray[] = new String[rowcount];
                String quantityarray[] = new String[rowcount];
                for (int i = 0; i < rowcount; i++) {
                    pidarray[i] = model.getValueAt(i, 0).toString();
                    quantityarray[i] = model.getValueAt(i, 2).toString();
                    vd.InsartVoucherDetails(pidarray[i], quantityarray[i], lvlvoucherno.getText());
                    String dbProQuantity = pro.productQuantityCheck(pidarray[i]);
                    int dbProQuantityint = Integer.parseInt(dbProQuantity);
                    int quantsub = Integer.parseInt(quantityarray[i]);
                    int qnew = dbProQuantityint - quantsub;
                    pro.updateProQuanAfterSale(pidarray[i], String.valueOf(qnew));
                }
                vc.InsartVoucherCustomer(tbxCustoname.getText(), tbxSearchMob.getText(), lvlTotalprice1.getText(), tbxPaid.getText(), lvlusernameshow.getText(),tbxDiscount.getText());
                pi.insertPaymentIns(lvlvoucherno.getText(),1, tbxPaid.getText());
                if (customerT == "new") {
                    cus.customerInsart(tbxCustoname.getText(), tbxSearchMob.getText(), tbxcustoaddress.getText());
                }
                JOptionPane.showMessageDialog(this, "Information Successfully Recoreded", "Done!", JOptionPane.INFORMATION_MESSAGE);
                
                String u = lvlusernameshow.getText();
                new SearchSaleDate(u,lvlvoucherno.getText()).setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Something is wrong", "invalid", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnFinaVoucherlActionPerformed

