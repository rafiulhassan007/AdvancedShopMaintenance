    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (!tbxsearch1.getText().equals("")) {
            String idsearch = "";
            String k = tbxsearch1.getText();
            String[] aux = k.split("_", 0);
            for (int i = 0; i < aux.length; i++) {
                if (aux.length == i + 1) {
                    idsearch = aux[i];
                }
            }
            tbxsearch.setText(idsearch);
        }
        ArrayList<ProductInfo> p = new ArrayList();
        p = pro.searchProductForSale(tbxsearch.getText());
        try {
            lvlproname.setText(p.get(0).getPname());
            lvlcname.setText(p.get(0).getCname());
            String qu = String.valueOf(p.get(0).getQuqntity());
            lvlquantity.setText(qu);
            String cost = p.get(0).getCost();
            String profit = p.get(0).getProfit();
            double costd = Double.parseDouble(cost);
            double profitd = Double.parseDouble(profit);
            double price = costd + (costd * (profitd / 100));
            lvlprice.setText(String.valueOf(price));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Product id invalid", "invalid", JOptionPane.ERROR_MESSAGE);
        }
        tbxsearch1.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if (!tbxquantity.getText().equals("") && !lvlquantity.getText().equals("")) {

            String q = tbxquantity.getText();
            String avq = lvlquantity.getText();
            int qi = Integer.parseInt(q);
            int avqi = Integer.parseInt(avq);
            double unitPrice = Double.parseDouble(lvlprice.getText());
            double price = qi * unitPrice;
            if (avqi >= qi) {
                try {
                    int selectedRowIndex = tblVoucher.getSelectedRow();
                    model.removeRow(selectedRowIndex);
                    tblselect = "";
                } catch (Exception e) {

                }
                Object[] row = {tbxsearch.getText(), lvlproname.getText(), qi, price};
                model.addRow(row);
                tbxsearch.setText("");
                lvlproname.setText("");
                tbxquantity.setText("");
                lvlquantity.setText("");
                lvlprice.setText("");
                lvlcname.setText("");
                int i = model.getRowCount();
                double priceintable = 0;
                String pricetbl[] = new String[i];
                for (int x = 0; x < i; x++) {
                    pricetbl[x] = model.getValueAt(x, 3).toString();
                    double p = Double.parseDouble(pricetbl[x]);
                    priceintable = priceintable + p;
                }
                lvlTotalprice.setText(String.valueOf(priceintable));
                if(tbxDiscount.getText().equals("")){
                    lvlTotalprice1.setText(lvlTotalprice.getText());
                }else
                {
                    double dsc=Double.parseDouble(tbxDiscount.getText());
                    lvlTotalprice1.setText(String.valueOf(priceintable-(priceintable*(dsc/100))));
                }
            } else {
                JOptionPane.showMessageDialog(this, "Invalid Quantity", "invalid", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Some Fields Empty", "invalid", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

